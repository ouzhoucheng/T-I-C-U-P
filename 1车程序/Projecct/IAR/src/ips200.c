void ips200_clear(uint16_t color)
{
  ips200_address_set(0, 0, IPS200_X_MAX - 1, IPS200_Y_MAX - 1);
  for(uint16_t i = 0; i < IPS200_X_MAX; i ++)
    for (uint16_t j = 0;j < IPS200_Y_MAX; j ++)
      ips200_wr_data16(color);
}

