void ips200_w_data(uint8_t dat)			//写数据
{
  IPS200_DATAPORT = (dat << DATA_START_NUM) | (IPS200_DATAPORT & ~((uint32_t)(0xFF << DATA_START_NUM)));
}

void ips200_wr_reg(uint16_t command)	//写命令
{
  IPS200_RS_L;
  IPS200_RD_H;
  ips200_w_data(command);
  IPS200_CS_L;
  IPS200_WR_L;
  IPS200_WR_H;
  IPS200_CS_H; 
}

void ips200_wr_data(uint8_t dat)	                //向液晶屏写一个8位数据
{
  IPS200_RS_H;
  IPS200_RD_H;
  ips200_w_data(dat);
  IPS200_CS_L; 
  IPS200_WR_L;
  IPS200_WR_H;
  IPS200_CS_H; 	
}

void ips200_wr_data16(uint16_t dat)		//向液晶屏写一个16位数据
{
  IPS200_RS_H;
  IPS200_RD_H;
  ips200_w_data(dat >> 8);
  IPS200_CS_L;
  IPS200_WR_L;
  IPS200_WR_H;
  ips200_w_data(dat);
  IPS200_CS_L;
  IPS200_WR_L;
  IPS200_WR_H;
  IPS200_CS_H; 	 	
}

void ips200_w_reg(uint8_t com, uint8_t dat)		//写寄存器
{
  ips200_wr_reg(com);
  ips200_wr_data(dat);
}

void ips200_address_set(uint16_t x1, uint16_t y1, uint16_t x2, uint16_t y2)
{ 
  ips200_wr_reg(0x2A);
  ips200_wr_data16(x1);
  ips200_wr_data16(x2);
  ips200_wr_reg(0x2B);
  ips200_wr_data16(y1);
  ips200_wr_data16(y2);
  ips200_wr_reg(0x2C);
}

